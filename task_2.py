a=int(input("enter the number a:-"))
b=int(input("enter the number b:-"))
op=input("enter an operation:-")

if op=='+':
    print("addition the numbers:",a+b)
elif op=='-':
    print("subtract the numbers:",a-b)
elif op=='*':
    print("multiply the numbers:",a*b)
elif op=='/':
    print("divide the numbers:",a/b)